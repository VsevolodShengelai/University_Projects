package com.example.lab41lagrangepolinomgui;

public class Coordinates {
    double x;
    double y;

    public Coordinates( double x, double y){
        this.x = x;
        this.y = y;
    }
    public double getX(){
        return this.x;
    }
    public double getY() { return this.y; }

    public double setX(){
        return this.x = x;
    }
    public double setY() { return this.y = y; }



}
